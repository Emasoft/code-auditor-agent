def _write_fixture(fixture_dir: Path, files: dict[str, str]) -> None:
    for relpath, content in files.items():
        f = fixture_dir / relpath
        f.parent.mkdir(parents=True, exist_ok=True)
        f.write_text(content, encoding="utf-8")


def _run_detector(spec: StepSpec, fixture_dir: Path, out_dir: Path) -> subprocess.CompletedProcess:
    if spec.cli_form == "module":
        cmd = [
            sys.executable, "-m",
            "scripts.prereview." + spec.script.removesuffix(".py"),
            str(fixture_dir), str(out_dir),
        ]
    elif spec.cli_form == "diff":
        diff_path = fixture_dir / "fake.diff"
        cmd = [sys.executable, str(SCRIPTS / spec.script), str(diff_path), str(out_dir)]
    else:
        cmd = [
            sys.executable, str(SCRIPTS / spec.script),
            str(fixture_dir), str(out_dir), *spec.extra_args,
        ]
    return subprocess.run(
        cmd, capture_output=True, text=True,
        cwd=str(PLUGIN_ROOT), timeout=300,
    )


def _load_report(out_dir: Path) -> dict:
    json_files = sorted(out_dir.glob("*.json"))
    assert json_files, f"detector wrote no JSON in {out_dir}"
    return json.loads(json_files[0].read_text(encoding="utf-8"))


