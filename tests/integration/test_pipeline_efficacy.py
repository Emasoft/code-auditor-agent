"""Pipeline-efficacy integration tests (TRDD-7e364ace, all 24 steps).

For every script-backed step in the bug-detection pipeline, this test
plants the EXACT bug patterns the detector is supposed to catch into a
fixture directory, invokes the detector as a subprocess (the same way
the pipeline does), and asserts that every expected finding-code
actually fires.

Quality-gate semantics (per user directive 2026-05-17):
- This test is a hard gate. A single missed code fails the test.
- CI blocks merge on failure; publish.py blocks release on failure.
- No push goes out without zero missed issues.

Steps whose detector is an LLM agent (1, 2, 3, 4, 17, 18, 20, 21, 22,
23) cannot be mechanically exercised from a one-shot harness — those
remain documented in agents/ but are not gated here.

Run locally:
    uv run pytest tests/integration/ -v
"""

