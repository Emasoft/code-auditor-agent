def _spec_id(spec: StepSpec) -> str:
    return f"step-{spec.step:02d}-{spec.name}"


@pytest.mark.parametrize("spec", ALL_SPECS, ids=_spec_id)
def test_pipeline_step_detects_seeded_bugs(spec: StepSpec, tmp_path: Path) -> None:
    """Each step must catch every bug code it claims to catch.

    Fixture: seeded under ``tmp_path`` (outside the plugin's git repo, so
    ``git ls-files`` enumeration sees every file we plant).

    Gate: any expected code missing from the JSON output fails the test.
    """
    fixture_dir = tmp_path / "fixture"
    out_dir = tmp_path / "out"
    fixture_dir.mkdir()
    out_dir.mkdir()
    _write_fixture(fixture_dir, spec.files)

    proc = _run_detector(spec, fixture_dir, out_dir)
    assert proc.returncode == 0, (
        f"detector exited {proc.returncode}\n"
        f"stderr: {proc.stderr[-500:]}"
    )

    report = _load_report(out_dir)

    if spec.step == 0:
        # Domain detection — verify every seeded language/domain is detected.
        langs = report.get("languages", {})
        domains = report.get("domains", {})
        expected_langs = {"python", "typescript", "go", "rust",
                          "swift", "elixir", "solidity"}
        expected_domains = {"docker", "graphql"}
        missed_langs = {ln for ln in expected_langs
                        if not langs.get(ln, {}).get("detected")}
        missed_doms = {dn for dn in expected_domains
                       if not domains.get(dn, {}).get("detected")}
        assert not missed_langs and not missed_doms, (
            f"Step 0 missed languages={sorted(missed_langs)} "
            f"domains={sorted(missed_doms)}"
        )
        return

    if spec.step == 16:
        # Type-design gate — must surface NewPublic and Another, not _private.
        found = {t.get("name") for t in report.get("types", [])}
        assert "NewPublic" in found, f"gate missed NewPublic; got {sorted(found)}"
        assert "Another" in found, f"gate missed Another; got {sorted(found)}"
        assert "_private" not in found, (
            f"gate leaked private symbol _private; got {sorted(found)}"
        )
        return

    if spec.step == 5:
        # Linter pre-flight — at least one external tool must run.
        # If `ruff` isn't on PATH, skip rather than fail (host-dependent).
        tools_run = report.get("tools_run") or []
        if not tools_run:
            pytest.skip(
                "No external linters installed on PATH; "
                "step 5 efficacy depends on the host environment."
            )
        # If ruff ran, it must have produced findings on the seeded `bad.py`.
        by_tool = report.get("by_tool_count", {})
        if "ruff" in tools_run:
            assert by_tool.get("ruff", 0) > 0, (
                "ruff ran but found nothing on the seeded bad.py — "
                "either the fixture is stale or ruff config is off"
            )
        return

    # General case — every expected code must fire ≥1 time.
    findings = report.get("findings", [])
    found_codes = {f.get("code") for f in findings}
    expected = set(spec.expected_codes)
    missing = expected - found_codes
    assert not missing, (
        f"Step {spec.step} ({spec.name}) detector missed "
        f"{len(missing)} expected code(s): {sorted(missing)}\n"
        f"  Codes found: {sorted(found_codes)}\n"
        f"  Total findings: {len(findings)}\n"
        f"  Fixture dir: {fixture_dir}\n"
        f"  This is a QUALITY-GATE failure — the detector regressed."
    )


def test_all_24_steps_accounted_for() -> None:
    """Sanity check: every step number is either mechanically tested here
    or documented as agent-only.

    Steps 0-23 = 24 total. This test fails if a new step gets added to
    the TRDD without updating either ALL_SPECS or AGENT_ONLY.
    """
    mechanical = {s.step for s in ALL_SPECS}
    agent_only = {1, 2, 3, 4, 17, 18, 20, 21, 22, 23}
    covered = mechanical | agent_only
    expected = set(range(24))
    missing = expected - covered
    assert not missing, (
        f"Step(s) {sorted(missing)} are not accounted for in either "
        f"the mechanical-test specs or the agent-only allow-list. "
        f"If you added a step to TRDD-7e364ace, update this test."
    )
