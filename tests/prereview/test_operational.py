"""Unit tests for Step 19 — operational scanner."""

from __future__ import annotations

import json
from pathlib import Path

from scripts.prereview import operational as op


def _write(p: Path, content: str = "") -> None:
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(content, encoding="utf-8")


def _list_file(p: Path, items: list[str]) -> Path:
    _write(p, "\n".join(items) + "\n")
    return p


# ---- schema / codegen drift -----------------------------------------------


def test_proto_change_without_generated_flagged(tmp_path: Path) -> None:
    _write(tmp_path / "api" / "users.proto", "message User { string id = 1; }\n")
    _write(tmp_path / "api" / "other.py", "x = 1\n")  # not @generated
    listing = _list_file(tmp_path / "pr.txt", ["api/users.proto", "api/other.py"])
    result = op.detect(tmp_path, pr_files=op._load_pr_files(tmp_path, listing))
    codes = {f["code"] for f in result["findings"]}
    assert "SCHEMA_NO_CODEGEN" in codes


def test_proto_change_with_generated_not_flagged(tmp_path: Path) -> None:
    _write(tmp_path / "api" / "users.proto", "message User { string id = 1; }\n")
    _write(
        tmp_path / "api" / "users_pb2.py",
        "# @generated by protoc-gen-python\n# DO NOT EDIT\n",
    )
    listing = _list_file(tmp_path / "pr.txt", ["api/users.proto", "api/users_pb2.py"])
    result = op.detect(tmp_path, pr_files=op._load_pr_files(tmp_path, listing))
    codes = {f["code"] for f in result["findings"]}
    assert "SCHEMA_NO_CODEGEN" not in codes


