# ---- determinism + CLI ----------------------------------------------------


def test_findings_deterministically_sorted(tmp_path: Path) -> None:
    _write(tmp_path / "api" / "a.proto", "message A {}\n")
    _write(tmp_path / "api" / "b.proto", "message B {}\n")
    listing = _list_file(tmp_path / "pr.txt", ["api/a.proto", "api/b.proto"])
    pr_files = op._load_pr_files(tmp_path, listing)
    r1 = op.detect(tmp_path, pr_files)
    r2 = op.detect(tmp_path, pr_files)
    r1.pop("timestamp")
    r2.pop("timestamp")
    assert json.dumps(r1, sort_keys=True) == json.dumps(r2, sort_keys=True)


def test_main_emits_file(tmp_path: Path) -> None:
    repo = tmp_path / "repo"
    out = tmp_path / "out"
    _write(repo / "Dockerfile", "FROM python:3.12-slim\n")
    _write(repo / "src" / "main.py", "x = 1\n")
    listing = repo / "pr.txt"
    listing.write_text("Dockerfile\nsrc/main.py\n", encoding="utf-8")
    rc = op.main(["operational", str(repo), str(out), "--pr-files-from", str(listing)])
    assert rc == 0
    body = json.loads(next(out.iterdir()).read_text(encoding="utf-8"))
    assert body["schema_version"] == 1
